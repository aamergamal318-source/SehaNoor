/* ── Scroll: progress + back-to-top + header shadow ── */
const scrollProg = document.getElementById("scrollProg");
const bttBtn     = document.getElementById("bttBtn");
const hdr        = document.getElementById("hdr");
const readLine   = document.getElementById("readingLine");

window.addEventListener("scroll", () => {
  const docH = document.body.scrollHeight - window.innerHeight;
  const pct  = docH > 0 ? (window.scrollY / docH) * 100 : 0;
  if (scrollProg) scrollProg.style.width = pct + "%";
  if (readLine)   readLine.style.width   = pct + "%";
  if (bttBtn)  bttBtn.classList.toggle("visible", window.scrollY > 300);
  if (hdr)     hdr.classList.toggle("scrolled", window.scrollY > 60);
}, { passive:true });

if (bttBtn) bttBtn.addEventListener("click", () => window.scrollTo({ top:0, behavior:"smooth" }));

